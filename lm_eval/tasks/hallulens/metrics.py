def _items_cache_key(items):
    """
    Stable key based ONLY on prompt (ignore completion).
    Assumes same prompt → same evaluation intent.
    """
    prompts = []

    for item in items:
        doc = item["doc"]
        prompts.append(doc.get("prompt", "").strip())

    prompts.sort()
    return tuple(prompts)

