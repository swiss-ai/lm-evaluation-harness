def hallu_rate_agg(items, max_workers=64):
    results = _run_all(items, max_workers)
    scores = [r["hallu_rate"] for r in results if not np.isnan(r.get("hallu_rate", np.nan))]
    return sum(scores) / len(scores) if scores else np.nan


